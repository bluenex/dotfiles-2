#!/bin/bash

cd "$(dirname "${BASH_SOURCE[0]}")" \
    && . "utils.sh"

# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

create_directories() {

    declare -a DIRECTORIES=(
        "$HOME/gitRepo/_temp"
        "$HOME/gitRepo/_salween"
        "$HOME/gitRepo/_hobbies"
        "$HOME/gitRepo/_side-projects"
    )

    for i in "${DIRECTORIES[@]}"; do
        mkd "$i"
    done

}

# - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - - -

main() {
    print_in_purple "\n â€¢ Create directories\n\n"
    create_directories
}

main
